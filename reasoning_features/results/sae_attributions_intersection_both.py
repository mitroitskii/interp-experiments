def compare_four_lists(name_prefix, list_out_base, list_out_zero, list_data_base, list_data_zero):
    """Compares four lists (outputs base, outputs zero, data base, data zero)."""

    set_out_base = set(list_out_base)
    set_out_zero = set(list_out_zero)
    set_data_base = set(list_data_base)
    set_data_zero = set(list_data_zero)

    # 1. Intersection of all four
    intersection_all = sorted(list(set_out_base & set_out_zero & set_data_base & set_data_zero))

    # Combined sets for groups
    set_outputs_union = set_out_base | set_out_zero
    set_data_union = set_data_base | set_data_zero
    set_outputs_intersect = set_out_base & set_out_zero
    set_data_intersect = set_data_base & set_data_zero

    # 2. Elements in BOTH outputs but NEITHER data list
    unique_to_outputs_group = sorted(list(set_outputs_intersect - set_data_union))

    # 3. Elements in BOTH data lists but NEITHER output list
    unique_to_data_group = sorted(list(set_data_intersect - set_outputs_union))

    # 4. Elements unique to each list
    unique_out_base = sorted(list(set_out_base - (set_out_zero | set_data_base | set_data_zero)))
    unique_out_zero = sorted(list(set_out_zero - (set_out_base | set_data_base | set_data_zero)))
    unique_data_base = sorted(list(set_data_base - (set_out_base | set_out_zero | set_data_zero)))
    unique_data_zero = sorted(list(set_data_zero - (set_out_base | set_out_zero | set_data_base)))

    print(f"--- Comparison for {name_prefix} ---")
    print(f"Intersection (All 4) ({len(intersection_all)}): {intersection_all}")
    print(f"Unique to Outputs Group (In both Outputs, not in Data) ({len(unique_to_outputs_group)}): {unique_to_outputs_group}")
    print(f"Unique to Data Group (In both Data, not in Outputs) ({len(unique_to_data_group)}): {unique_to_data_group}")
    print(f"Unique to Outputs Base ({len(unique_out_base)}): {unique_out_base}")
    print(f"Unique to Outputs Zero ({len(unique_out_zero)}): {unique_out_zero}")
    print(f"Unique to Data Base ({len(unique_data_base)}): {unique_data_base}")
    print(f"Unique to Data Zero ({len(unique_data_zero)}): {unique_data_zero}")
    print("-" * (len(f"--- Comparison for {name_prefix} ---")) + "\n")


